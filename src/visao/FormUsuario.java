    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        
         if (txtUsuario.getText().isEmpty()){
        JOptionPane.showMessageDialog(null," Preencha o USUARIO para continua ");
          txtUsuario.requestFocus();
          }else if (txtConfirmaSenha.getText().isEmpty()) {
          JOptionPane.showMessageDialog(null," Confirma a SENHA para continua ");
          txtConfirmaSenha.requestFocus();
          } else if (txtSenha.getText().isEmpty()){
              JOptionPane.showMessageDialog(null," Preencha a SENHA para continua "); 
              txtSenha.requestFocus();
          } else
          if ( flag == 1){
        mod.setUsuNome(txtUsuario.getText());
        mod.setUsuSenha(txtSenha.getText());
        mod.setUsuTipo((String)jComboBoxTipo.getSelectedItem());
        
        dao.Salvar(mod);
        txtUsuario.setText("");
        txtSenha.setText("");
        txtConfirmaSenha.setText("");
         txtUsuario.setEnabled(false);
        txtSenha.setEnabled(false);
        jComboBoxTipo.setEnabled(false);
        txtConfirmaSenha.setEnabled(false);
        btnSalvar.setEnabled(true); 
    }else {
        mod.setUsuNome(txtUsuario.getText());
        mod.setUsuSenha(txtSenha.getText());
        mod.setUsuTipo((String)jComboBoxTipo.getSelectedItem());
       
       dao.Editar(mod);
        txtUsuario.setText("");
        txtSenha.setText("");
        txtConfirmaSenha.setText("");
         txtUsuario.setEnabled(false);
        txtSenha.setEnabled(false);
        jComboBoxTipo.setEnabled(false);
        txtConfirmaSenha.setEnabled(false);
        btnSalvar.setEnabled(false); 
          
          }
    }//GEN-LAST:event_btnSalvarActionPerformed
    
    
